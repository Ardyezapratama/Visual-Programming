    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Buku");
        model.addColumn("NAma Buku");
        model.addColumn("Jenis Buku");
        model.addColumn("Pengarang");
        
        //show
        try {
            String sql = "SELECT * FROM tb_buku";
            java.sql.Connection connect = (Connection)Conn.configDB();
            java.sql.Statement stm = connect.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4),});
               
            }
            jTableBuku.setModel(model);
        } catch (Exception e) {
        }
    }
