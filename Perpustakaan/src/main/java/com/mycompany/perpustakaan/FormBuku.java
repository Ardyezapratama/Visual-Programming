    private void jBtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTambahActionPerformed
        try {
            String sql = "INSERT INTO tb_buku VALUES ('" + jTfIdBuku.getText() + "', '" + jTfNamaBuku.getText() + "', '" + jCbJenisBuku.getSelectedItem() + "', '" + jTfPengarang.getText()+"')";
            java.sql.Connection connect = (Connection)Conn.configDB();
            java.sql.PreparedStatement pst = connect.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            load_table();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jBtnTambahActionPerformed

    private void jTableBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableBukuMouseClicked
        jTfIdBuku.enable(false);
        int baris = jTableBuku.rowAtPoint(evt.getPoint());
        String idBuku = jTableBuku.getValueAt(baris, 0).toString();
        jTfIdBuku.setText(idBuku);
        
        String namaBuku = jTableBuku.getValueAt(baris, 1).toString();
        jTfNamaBuku.setText(namaBuku);
        
        String jenisBuku = jTableBuku.getValueAt(baris, 2).toString();
        if (jenisBuku.equals("Novel")){
            jCbJenisBuku.setSelectedIndex(0);
        }else if (jenisBuku.equals("Komik")){
            jCbJenisBuku.setSelectedIndex(1);
        }else if (jenisBuku.equals("IT")){
            jCbJenisBuku.setSelectedIndex(2);
        }else if (jenisBuku.equals("Pemrograman")){
            jCbJenisBuku.setSelectedIndex(3);
        }
        
        String pengarang = jTableBuku.getValueAt(baris, 3).toString();
        jTfPengarang.setText(pengarang);
        
    }//GEN-LAST:event_jTableBukuMouseClicked

    private void jBtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditActionPerformed
        try {
            String sql = "UPDATE tb_buku SET nama = '" + jTfNamaBuku.getText() + "', jenis = '" + jCbJenisBuku.getSelectedItem() + "', pengarang = '" +jTfPengarang.getText() + "' WHERE id = '" + jTfIdBuku.getText() + "'";
            java.sql.Connection connect = (Connection)Conn.configDB();
            java.sql.PreparedStatement pst = connect.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Edit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Edit" + e.getMessage());
        }
        load_table();
    }//GEN-LAST:event_jBtnEditActionPerformed

