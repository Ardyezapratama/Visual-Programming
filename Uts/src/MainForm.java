    private void jBtnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnInputActionPerformed
        DefaultTableModel table = (DefaultTableModel) jTableBar.getModel();
        String numb = "UNT_";
        int lable = 1;
        for(int i = 0; i < table.getRowCount(); i++){
            lable += 1;
        }
        
        List list = new ArrayList<>();
        jTableBar.getAutoCreateColumnsFromModel();
        list.add(numb + lable);
        list.add(jCbNamaBar.getSelectedItem().toString());
        list.add(jLabelHarga.getText());
        list.add(jTfJum.getText());
        
        
            int harga,total,hasil;
            harga = Integer.parseInt(jLabelHarga.getText());
            total = Integer.parseInt(jTfJum.getText());
            hasil = harga * total;
            list.add(hasil);
            table.addRow(list.toArray()); 
    }//GEN-LAST:event_jBtnInputActionPerformed

