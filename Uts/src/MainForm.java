    private void jBtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTotalActionPerformed
        DefaultTableModel table = (DefaultTableModel) jTableBar.getModel();
        
        int total = 0;
        for (int i = 0; i < table.getRowCount(); i++){
        int nilai = (int) table.getValueAt(i, 4);
        total += nilai;
        }
        
        if(jRbCash.isSelected()){
            jLabelTotal.setText(String.valueOf(total));
        }else{
            jLabelTotal.setText(String.valueOf(total + (total * 0.05)));
        }
    }//GEN-LAST:event_jBtnTotalActionPerformed

